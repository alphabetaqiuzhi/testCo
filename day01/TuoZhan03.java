package day01;

public class TuoZhan03 {

	public static void main(String[] args) {
		byte a1 = 10;
		System.out.println("整数类型-byte:"+a1);
		
		short a2 = 100;
		System.out.println("整数类型-short:"+a2);
		
		int a3 = 1000;
		System.out.println("整数类型-int:"+a3);
		
		long a4 = 10000L;
		System.out.println("整数类型-long:"+a4);
		
		float a5 = 100000.0F;
		System.out.println("小数类型-float:"+a5);
		
		double a6 = 1000000.0;
		System.out.println("小数类型-double:"+a6);
		
		char a7 = 'Z';
		System.out.println("字符类型-char:"+a7);
		
		boolean a8 = false;
		System.out.println("布尔类型-boolean:"+a8);





		
	}
}
